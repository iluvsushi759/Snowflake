-- Create medical research tables in Research_Landing schema
CREATE OR REPLACE TABLE Research_Landing.PATIENTS (
    PATIENT_ID INT,
    FIRST_NAME STRING,
    LAST_NAME STRING,
    DOB DATE,
    GENDER STRING,
    ENROLLMENT_DATE DATE
);

CREATE OR REPLACE TABLE Research_Landing.TRIALS (
    TRIAL_ID INT,
    TRIAL_NAME STRING,
    START_DATE DATE,
    END_DATE DATE,
    PHASE STRING,
    CONDITION STRING
);

CREATE OR REPLACE TABLE Research_Landing.PARTICIPATION (
    PARTICIPATION_ID INT,
    PATIENT_ID INT,
    TRIAL_ID INT,
    STATUS STRING,
    CONSENT_DATE DATE
);
